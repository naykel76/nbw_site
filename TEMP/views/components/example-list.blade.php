<h4>Bullet Points Can Make Your Website More User Friendly</h4>

<p>It's difficult to write articles that hold the reader's attention. Bullet lists are a great way to draw the readers attention to vital facts on a specific top or concept. Here are a few tips for creating an effective list:</p>

<ul class="ml">
    <li>Use bullet lists to to draw the user's attention to vital facts on a specific top or concept.</li>
    <li>You want your content to look like an article not a grocery list so don't over do it.</li>
    <li>Be clear and concise to cut to the chase.</li>
    <li>Use bullet lists to help users better understand complicated concepts or ideas.</li>
    <li>Think of a bullet point as a mini headline.</li>
</ul>

